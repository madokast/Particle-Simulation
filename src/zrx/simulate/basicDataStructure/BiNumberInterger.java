package zrx.simulate.basicDataStructure;

public class BiNumberInterger {
    public int x;
    public int y;

    public BiNumberInterger(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "BiNumberInterger{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
